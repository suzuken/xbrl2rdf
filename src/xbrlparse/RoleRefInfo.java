package xbrlparse;

public class RoleRefInfo {

	private String roleURI;
	private String type;
	private String absoluteHref;
	private String uri;
	private String fragment;

	public RoleRefInfo(String roleURI, String type, String absoluteHref,
			String uri, String fragment) {
		this.roleURI = roleURI;
		this.type = type;
		this.absoluteHref = absoluteHref;
		this.uri = uri;
		this.fragment = fragment;
	}

	@Override
	public String toString() {
		return "RoleRefInfo [absoluteHref=" + absoluteHref + ", fragment="
				+ fragment + ", roleURI=" + roleURI + ", type=" + type
				+ ", uri=" + uri + "]";
	}

	public String getRoleURI() {
		return roleURI;
	}

	public void setRoleURI(String roleURI) {
		this.roleURI = roleURI;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getAbsoluteHref() {
		return absoluteHref;
	}

	public void setAbsoluteHref(String absoluteHref) {
		this.absoluteHref = absoluteHref;
	}

	public String getUri() {
		return uri;
	}

	public void setUri(String uri) {
		this.uri = uri;
	}

	public String getFragment() {
		return fragment;
	}

	public void setFragment(String fragment) {
		this.fragment = fragment;
	}

}
